import { NavLink, Outlet, useNavigate } from 'react-router-dom'
import { useEffect } from 'react'
import useAuthStore from '../stores/authStore'
import useDashboardStore from '../stores/dashboardStore'
import BillingBanner from './BillingBanner'

const navItems = [
  { to: '/', label: 'Dashboard', icon: DashboardIcon, end: true },
  { to: '/negocios', label: 'Neg\u00f3cios', icon: NegociosIcon },
  { to: '/approvals', label: 'Aprova\u00e7\u00f5es', icon: ApprovalsIcon, badge: true },
  { to: '/content-ai', label: 'Content AI', icon: ContentAIIcon },
  { to: '/crm', label: 'Contatos', icon: ContactsIcon, end: true },
  { to: '/crm/funnel', label: 'Funil', icon: FunnelIcon },
  { to: '/ads', label: 'An\u00fancios', icon: AdsIcon },
  { to: '/benchmark', label: 'Benchmark', icon: BenchmarkIcon },
  { to: '/media', label: 'Banco de M\u00eddia', icon: MediaIcon },
  { to: '/history', label: 'Hist\u00f3rico', icon: HistoryIcon },
  { to: '/settings/billing', label: 'Plano', icon: BillingIcon },
  { to: '/settings', label: 'Configura\u00e7\u00f5es', icon: SettingsIcon, end: true },
]

export default function Layout() {
  const { user, logout } = useAuthStore()
  const pendingCount = useDashboardStore((s) => s.pendingCount)
  const fetchPendingVideos = useDashboardStore((s) => s.fetchPendingVideos)
  const navigate = useNavigate()

  useEffect(() => {
    fetchPendingVideos()
    const interval = setInterval(fetchPendingVideos, 60000)
    return () => clearInterval(interval)
  }, [fetchPendingVideos])

  const handleLogout = () => {
    logout()
    navigate('/login')
  }

  return (
    <div className="flex h-screen bg-gray-50">
      {/* Sidebar */}
      <aside className="w-64 bg-gray-900 text-white flex flex-col">
        <div className="p-6 border-b border-gray-700">
          <h1 className="text-lg font-bold tracking-tight">Usina do Tempo</h1>
          <p className="text-xs text-gray-400 mt-1">Plataforma de Marketing</p>
        </div>

        <nav className="flex-1 py-4">
          {navItems.map((item) => (
            <NavLink
              key={item.to}
              to={item.to}
              end={item.end || false}
              className={({ isActive }) =>
                `flex items-center gap-3 px-6 py-3 text-sm transition-colors ${
                  isActive
                    ? 'bg-indigo-600 text-white'
                    : 'text-gray-300 hover:bg-gray-800 hover:text-white'
                }`
              }
            >
              <item.icon />
              <span>{item.label}</span>
              {item.badge && pendingCount > 0 && (
                <span className="ml-auto bg-red-500 text-white text-xs font-bold px-2 py-0.5 rounded-full">
                  {pendingCount}
                </span>
              )}
            </NavLink>
          ))}
        </nav>

        {/* User section */}
        <div className="p-4 border-t border-gray-700">
          <div className="flex items-center gap-3">
            <div className="w-8 h-8 rounded-full bg-indigo-500 flex items-center justify-center text-sm font-bold">
              {user?.papel?.[0]?.toUpperCase() || 'U'}
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-sm font-medium truncate">{user?.papel || 'Usuário'}</p>
              <p className="text-xs text-gray-400 truncate">{user?.id?.slice(0, 8)}...</p>
            </div>
            <button
              onClick={handleLogout}
              title="Sair"
              className="text-gray-400 hover:text-white transition-colors"
            >
              <LogoutIcon />
            </button>
          </div>
        </div>
      </aside>

      {/* Main content */}
      <main className="flex-1 overflow-auto flex flex-col">
        <BillingBanner />
        <div className="flex-1 overflow-auto">
          <Outlet />
        </div>
      </main>
    </div>
  )
}

